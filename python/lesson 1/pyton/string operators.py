# String Operations

# Declare strings
str1 = "Codingal"
str2 = "Learnings"

# Concatenation and repetition
concatenated = str1 + str2
repeated = str1 * 5

# Display original strings
print("String 1 =", str1)
print("String 2 =", str2)

# Display results of operations
print("Concatenation of str1 and str2 =", concatenated)
print("Repetition of str1 (5 times) =", repeated)

# String analysis
print("Length of str1 =", len(str1))
print("First character of str1 =", str1[0])
print("Last character of str1 =", str1[-1])
print("Sliced part of str1 (index 2 to 5) =", str1[2:6])
