# Swapping Two Numbers (Using a Temporary Variable)

# Initial values
n1 = 120
n2 = 340

# Display before swapping
print("Before Swapping:")
print("n1 =", n1)
print("n2 =", n2)

# Swapping process
temp = n1
n1 = n2
n2 = temp

# Display after swapping
print("After Swapping:")
print("n1 =", n1)
print("n2 =", n2)
